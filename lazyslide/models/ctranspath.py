class CTransPathFeatures:
    """
    CTransPath pretrained feature extractor.

    The feature extractor is based on a modified swin transformer
    ("swin_tiny_patch4_window7_224"), pretrained on digital pathology images,
    224 x 224 px, at ~10x magnification.

    Feature dimensions: 768

    Manuscript: Transformer-based unsupervised contrastive learning for
    histopathological image classification. Medical Image Analysis. Oct 2022.
    https://doi.org/10.1016/j.media.2022.102559

    GitHub: https://github.com/Xiyue-Wang/TransPath
    """

    tag = "ctranspath"
    license = """GNU General Public License v3.0"""
    citation = """
@{wang2022,
  title={Transformer-based Unsupervised Contrastive Learning for Histopathological Image Classification},
  author={Wang, Xiyue and Yang, Sen and Zhang, Jun and Wang, Minghui and Zhang, Jing  and Yang, Wei and Huang, Junzhou  and Han, Xiao},
  journal={Medical Image Analysis},
  year={2022},
  publisher={Elsevier}
}
"""

    def __init__(self, device=None, center_crop=False):
        try:
            from huggingface_hub import hf_hub_download
        except (ImportError, ModuleNotFoundError):
            raise ModuleNotFoundError(
                "Install huggingface-hub with `pip install huggingface-hub`."
            )

        if device is None:
            device = "cuda" if torch.cuda.is_available() else "cpu"
        self.device = device
        self.model = _build_ctranspath_model()
        self.model.head = torch.nn.Identity().to(self.device)

        checkpoint_path = hf_hub_download(
            repo_id="jamesdolezal/CTransPath", filename="ctranspath.pth"
        )
        td = torch.load(checkpoint_path, map_location=self.device)
        self.model.load_state_dict(td["model"], strict=True)
        self.model = self.model.to(self.device)
        self.model.eval()

        # ---------------------------------------------------------------------
        self.num_features = 768
        all_transforms = [transforms.CenterCrop(224)] if center_crop else []
        all_transforms += [
            transforms.Lambda(lambda x: x / 255.0),
            transforms.Normalize(mean=(0.485, 0.456, 0.406), std=(0.229, 0.224, 0.225)),
        ]
        self.transform = transforms.Compose(all_transforms)
        self.preprocess_kwargs = dict(standardize=False)
        self._center_crop = center_crop
        # ---------------------------------------------------------------------

    def __call__(self, *args, **kwargs):
        return self.model(*args, **kwargs)
